x = int(1)   # x Will Be 1
y = int(2.8) # y Will Be 2
z = int("3") # z Will Be 3

print("Int")
print(x)
print(y)
print(z)

print("\n")

x = float(1)     # x Will Be 1.0
y = float(2.8)   # y Will Be 2.8
z = float("3")   # z Will Be 3.0
w = float("4.2") # w Will Be 4.2

print("Floats")
print(w)
print(x)
print(y)
print(z)

print("\n")

x = str("s1") # x Will Be 's1'
y = str(2)    # y Will Be '2'
z = str(3.0)  # z Will Be '3.0'

print("Strings")
print(x)
print(y)
print(z)
